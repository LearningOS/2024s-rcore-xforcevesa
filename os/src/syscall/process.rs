pub fn sys_get_time(_ts: *mut TimeVal, _tz: usize) -> isize {
    trace!(
        "kernel:pid[{}] sys_get_time",
        current_task().unwrap().pid.0
    );
    let us = get_time_us();
    let sec = us / 1000000;
    let usec = us % 1000000;
    *translated_refmut(current_user_token(), _ts) = TimeVal { sec, usec };
    0
}

